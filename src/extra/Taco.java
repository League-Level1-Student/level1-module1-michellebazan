package extra;
// Created Taco class
public class Taco {
	//Adding 2 private String member variable for meat and sauce
	private String meat;
	private String sauce; 
	
	
	//Creating methods getMeat() and getSauce() that return the member variables 
	//Getters
	public String getMeat() {
		return meat;
	}
	public String getSauce() {
		return sauce;
	}
	//Create more methods + other
	//Setters
	public void setMeat(String meat) {
		this.meat = meat;
	}
	public void setSauce(String sauce) {
		this.sauce = sauce;
	}
}
